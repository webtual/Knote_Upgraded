<script>
  const openBtn = document.getElementById("sidemenuopenbtn");
  const closeBtn = document.getElementById("sidemenuclosebtn");
  const navWrapper = document.querySelector(".mobile_nav_wrapper");
  const navSidebar = document.querySelector(".mobile_nav_sidebar");

  openBtn.addEventListener("click", () => {
    navWrapper.classList.add("active");
    navSidebar.classList.add("active");
  });

  closeBtn.addEventListener("click", () => {
    navWrapper.classList.remove("active");
    navSidebar.classList.remove("active");
  });
  // Select all menu items that have a dropdown
document.querySelectorAll(".mobile_nav_menu > li > a").forEach((menuLink) => {
    menuLink.addEventListener("click", function (e) {
        let parentLi = this.parentElement;

        // Check if this menu item has a dropdown
        if (parentLi.querySelector("ul")) {
            e.preventDefault(); // prevent navigation if it has submenu

            // Toggle "active" class on this menu item
            parentLi.classList.toggle("active");

            // Close siblings
            parentLi.parentElement.querySelectorAll("li").forEach((sibling) => {
                if (sibling !== parentLi) {
                    sibling.classList.remove("active");
                }
            });
        }
    });
});

</script>

